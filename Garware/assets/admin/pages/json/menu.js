var total = "";

function getmenu(manuitem) {
    var item = "";
    if (manuitem.length > 0) {
        manuitem.forEach(function (element) {
            item += "<li ><a href=" + element.link + "><span class='title'>" + element.tab + "</span>";
            if (element.sub != undefined) {
                item +="<span class='arrow'></span>";
            }
                item +="</a>";
            if (element.sub != undefined) {
                var items = element.sub;
                var subitem = "<ul class='sub-menu'>";
                subitem += getmenu(items);
                subitem += "</ul>";
                item += subitem;
            }
            item += "</li>";
        });
    }
    return item;
}
