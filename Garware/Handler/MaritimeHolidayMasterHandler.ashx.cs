using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using BaseClass;
using BuisnessController;
using Newtonsoft.Json;
namespace Garware.Handler
{
    /// <summary>
    /// Summary description for AreaOperationMasterHandler
    /// </summary>
    public class MaritimeHolidayMasterHandler : IHttpHandler
    {

        public void ProcessRequest(HttpContext context)
        {
            string jstring = string.Empty;
            System.IO.StreamReader str = new System.IO.StreamReader(context.Request.InputStream);
            string sBuf = str.ReadToEnd();
            dynamic postdata = JsonConvert.DeserializeObject(sBuf);
            string data = postdata.Method.ToString();



            switch (data)
            {
                case "InsertUpdate":

                    MARITIME_HOLIDAY_Base obj = new MARITIME_HOLIDAY_Base();
                    obj.MID  = Convert.ToInt32(postdata.id.ToString());
                    obj.DAY = Convert.ToInt32(postdata.DAY.ToString());
                    obj.MONTH = Convert.ToInt32(postdata.MONTH.ToString());
                    MARITIME_HOLIDAYBC.insertupdateData(obj);

                    jstring = JsonConvert.SerializeObject("Success");
                    break;

                case "Getdata":

                    jstring = JsonConvert.SerializeObject(MARITIME_HOLIDAYBC.getdata());
                    break;
            }

            context.Response.ContentType = "application / json";
            context.Response.Write(jstring);
        }

        public bool IsReusable
        {
            get
            {
                return false;
            }
        }
    }
}