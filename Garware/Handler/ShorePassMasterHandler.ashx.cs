using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using BaseClass;
using BuisnessController;
using Newtonsoft.Json;
namespace Garware.Handler
{
    /// <summary>
    /// Summary description for AreaOperationMasterHandler
    /// </summary>
    public class ShorePassMasterHandler : IHttpHandler
    {

        public void ProcessRequest(HttpContext context)
        {
            string jstring = string.Empty;
            System.IO.StreamReader str = new System.IO.StreamReader(context.Request.InputStream);
            string sBuf = str.ReadToEnd();
            dynamic postdata = JsonConvert.DeserializeObject(sBuf);
            string data = postdata.Method.ToString();



            switch (data)
            {
                case "InsertUpdate":

                    SHORE_PASS_MASTER_Base obj = new SHORE_PASS_MASTER_Base();
                    obj.SPID = Convert.ToInt32(postdata.SPID.ToString());
                    obj.CODE = Convert.ToString(postdata.CODE.ToString());
                    obj.DESCRIPTION = Convert.ToString(postdata.DESCRIPTION.ToString());
                    SHORE_PASS_MASTERBC.insertupdateData(obj);

                    jstring = JsonConvert.SerializeObject("Success");
                    break;

                case "Getdata":

                    jstring = JsonConvert.SerializeObject(SHORE_PASS_MASTERBC.getdata());
                    break;
            }

            context.Response.ContentType = "application / json";
            context.Response.Write(jstring);
        }

        public bool IsReusable
        {
            get
            {
                return false;
            }
        }
    }
}